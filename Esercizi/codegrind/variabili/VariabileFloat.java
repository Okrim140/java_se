package Esercizi.codegrind.variabili;

public class VariabileFloat {
	/*
	 * Scrivi un programma Java che dichiara una variabile di tipo float chiamata
	 * "altezza" e le assegna il valore 1.75. Stampa il valore della variabile.
	 */

	public static void main(String[] args) {
		float altezza = 1.75f;          //quando si assegna una fariabile di tipo float si mette la f alla fine del numeto
		System.out.println(altezza);

	}

}
